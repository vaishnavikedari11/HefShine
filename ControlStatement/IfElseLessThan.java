public class IfElseLessThan {
    public static void main(String[] args) {
        int num1= 12;
        int num2=4;

        if(num1<num2){
            System.out.println(num1+"is less than "+num2);
        }else{
            System.out.println(num2+"is less than"+num1);
        }

    }
}
