public class IfElseMax {
    public static void main(String[] args) {
        int num1=4;
        int num2=8;
        if(num1>num2){
            System.out.println(num1+" is maximum");
            System.out.println(num2+" is minimum");
        }else{
            System.out.println(num2+" is maximum");
            System.out.println(num1+" is minimum");
        }
    }
}
