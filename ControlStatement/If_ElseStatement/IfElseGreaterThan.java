public class IfElseGreaterThan {
    public static void main(String[] args) {
        int num1=14;
        int num2=89;
        if(num1>num2){
            System.out.println(num1+"is greater than "+num2);
        }else{
            System.out.println(num2+"is greater than "+num1);
        }
    }
}
