//give percentage and avg of 5 subjects
public class AverageOfSubject {
    public static void main(String[] args) {
        int s1=67,s2=56,s3=65,s4=25,s5=87;
        int avg=(s1+s2+s3+s4+s5)/5;
        System.out.println("average of subjects="+avg);
        int percentage=((s1+s2+s3+s4+s5)/5)*100;
        System.out.println("percentage of 5 subject="+percentage);
    }
}
